    private void btnSalvar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvar1MouseClicked
        // TODO add your handling code here:
        if (vazio()) {
            JOptionPane.showMessageDialog(null, "Informe a data!");
        }
        else if (txtHora1.getText().equals("00:00")) {
            JOptionPane.showMessageDialog(null, "Informe a hora de inicio!");
        }
        else if (txtHora2.getText().equals("00:00")) {
            JOptionPane.showMessageDialog(null, "Informe a hora de fim!");
        }
        else if (cmbTipo.getSelectedItem() == " ") {
            JOptionPane.showMessageDialog(null, "Informe o tipo de evento!");
        }
        else if (!rbAvulso.isSelected() && !rbMensal.isSelected()) {
            JOptionPane.showMessageDialog(null, "Informe a frequencia!");
        }
        
        Date data = txtData.getDate();
        DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = formatter.format(data);
        
        
        
        try {
            
            String conteudo = dataFormatada + "|" + cmbTipo.getSelectedItem().toString() + "|" + txtHora1.getText() + " ás " + txtHora2.getText() + "|" 
                + Frq + "|" + Salao + "\n";
        
            FileWriter file = new FileWriter("./src/Agendamentos.txt", true);
  
             file.write(conteudo);
             file.close();
             
             // Imprime confirmacao
            System.out.println("Agendamento Realizado!");
            
        } catch (IOException ex) {
           Logger.getLogger(AgendaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSalvar1MouseClicked

