    private void cmbTipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbTipoItemStateChanged
        // TODO add your handling code here:
        
        String item = cmbTipo.getSelectedItem().toString();
        
        if ("Aniversario".equals(item)) {
            rbSalao1.setEnabled(true);
            rbSalao2.setEnabled(true);
            rbQuadraFechada.setEnabled(true);
            Tipo = item;
        }
        else if ("Evento".equals(item)) {
            rbSalao1.setEnabled(true);
            rbSalao2.setEnabled(true);
            rbQuadraFechada.setEnabled(true);
            Tipo = item;
        }
        else{
           rbSalao1.setEnabled(false);
            rbSalao2.setEnabled(false);
            rbQuadraFechada.setEnabled(false); 
            Tipo = "Futebol";
        }

    }//GEN-LAST:event_cmbTipoItemStateChanged

