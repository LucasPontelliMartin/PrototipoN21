    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
       
        File arquivo = new File("./src/Agendamentos.txt" );
        if (arquivo.exists()) {
            String dadosConta[] = new String[4];
          String dados = new String();
            try {
                BufferedReader in = new BufferedReader(new FileReader(arquivo));

                while (in.ready()) {
                    
                    dadosConta = (in.readLine().split(Pattern.quote("|")));
                    dados += dadosConta[0] + " - "+ dadosConta[1]+" - "+ dadosConta[2] + " - " + dadosConta[3]+ " - " + dadosConta[4] + "\n" +
                            "─────────────────────────────────────────────" + "\n"; // linha a linha do txt
                    
                    txtAgendamentos.setText(dados);
                    
                }
                in.close();
            } catch (IOException ex) {
            Logger.getLogger(AgendaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        } else {
            System.out.println("Erro na leitura do arquivo.");
        }   
        
    }//GEN-LAST:event_formComponentShown

